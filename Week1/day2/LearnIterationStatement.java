package week1.day2;

public class LearnIterationStatement {

	public static void main(String[] args) {
		int j=99;
		            
		for (int i = 1; i <=10; i++) {
			//Loop no 1 : 
			//stage 1 : declaring and initializing --> i=11
			//stage 2 : condition expression--> true/false --> (11<=10)--> false
			//stage 3 : Iteration --> ++(increment) or --decrement --> use the value then update
			System.out.println("The value is : "+i);	
		}
		System.out.println(j);
		
	}

}
