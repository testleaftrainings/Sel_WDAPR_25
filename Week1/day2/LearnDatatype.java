package week1.day2;

public class LearnDatatype {

	public static void main(String[] args) {
		// declaring a variable
		// type //identifier // '=' to assign my value to the variable
		byte variableOne = 10;
		short variableTwo = 30000;
		int variableThree = 100000;
		long phoneNum = 9988776655l;

		float scitificExp = 647.747474f;
		double scitificExp2 = 647.74747463838383838;
		char letter = 'A';
		boolean condition = true;
		
		System.out.println("My data type is byte :"+variableOne);
		System.out.println("My data type is short :"+variableTwo);
		System.out.println("My data type is int :"+variableThree);
		System.out.println(phoneNum);
		System.out.println(scitificExp);
		System.out.println(scitificExp2);
		System.out.println(letter);
		System.out.println(condition);

	}

}
